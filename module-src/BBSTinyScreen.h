// BBSTinyScreen — Minimal SSD1306 OLED text driver for BBS status display
//
// ~3KB flash instead of ~150KB for the full Meshtastic Screen framework.
// Drives the SSD1306 (128x64 or 128x32) via I2C using a built-in 5x7 font.
// No dependencies on OLEDDisplay, GFX libraries, or Meshtastic's Screen class.
//
// Usage:
//   BBSTinyScreen screen;
//   screen.begin();        // auto-detects 0x3C or 0x3D
//   screen.clear();
//   screen.printAt(0, 0, "TinyBBS");
//   screen.display();      // flush to OLED
//
// For boards with MESHTASTIC_EXCLUDE_SCREEN=1 that still want a status display.

